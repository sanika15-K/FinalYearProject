package com.example.finalyearproject;

public class StudentAuthHelper {

    public String uecNumber;
    public String email;
    public String branch;
    public String name;

    public StudentAuthHelper() { }

    public StudentAuthHelper(String uecNumber, String email, String branch, String name) {
        this.uecNumber = uecNumber;
        this.email = email;
        this.branch = branch;
        this.name = name;
    }
}
